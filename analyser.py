    def __find_reports(self, text):
        patterns = [r"\bگزارش (\w+ \w+)\b"]
        reports = []
        for pattern in patterns:
            for matched in re.finditer(pattern, text):
                if matched is not None:
                    reports.append({
                        'type': 'گزارش',
                        'marker': matched.group(1),
                        'span': [x + 1 for x in matched.span()]
                    })

        return reports

